package BuilderConfig.mdf;

public class Peca {
    private int larg;
    private int prof;
    private int esp;
    private int acabamento;

    public int getLarg() {
        return larg;
    }

    public void setLarg(int larg) {
        this.larg = larg;
    }

    public int getProf() {
        return prof;
    }

    public void setProf(int prof) {
        this.prof = prof;
    }

    public int getEsp() {
        return esp;
    }

    public void setEsp(int esp) {
        this.esp = esp;
    }

    public int getAcabamento() {
        return acabamento;
    }

    public void setAcabamento(int acabamento) {
        this.acabamento = acabamento;
    }
}
